
public class Destino {
    private Integer cod;
    private String nomeCidade;

    public Destino(int cod, String nomeCidade) {
        this.cod = cod;
        this.nomeCidade = nomeCidade;
    }

    public Integer getCod() {
        return cod;
    }
    public String getNomeCidade() {
        return nomeCidade;
    }
}
